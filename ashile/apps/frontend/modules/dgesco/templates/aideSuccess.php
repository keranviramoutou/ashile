<p><u>Enquête Dgesco > Saisie en masse</u> : ce choix permet de saisir les réponses aux questions pour l'ensemble des élèves</p>
<p> - si une réponse à un degré égale à 1 ou 2 , alors la réponse affichée sera celle correspondant au degré de l'établissement 
de la scolarisation oridinaire en cours, il est à préciser dans ce cas que chaque réponse à la question concernée doit avoir le degré renseigné
avec la valeur 1 ou 2</p>
<p> - si le degré  de toutes les réponses à une question est égal à 0 alors toutes les réponses possibles seront affichées.<br>
il est à préciser dans ce cas que chaquee réponse à la question concernée doit avoir le degré renseigné à 0</p>


<p> - <u>Pour effacer une réponse</u>, il faut cliquer sur l'élève ,aller sur l'onglet DGESCO ,cliquer sur la question  dans le champs Sélectionner une réponse
choisir la valeur "vide"  puis enregister </p>


<p> <u>Le bouton intitulé 'Compléter sans objet" </u> permet de passer toutes les questions de l'élève sans réponse à la valeur "SANS OBJET" ,ce qui permet de faire la différence entre non renseignée (pas de réponse encore saisie
et "SANS OBJET":  on n'a traité la question mais l'élève n'est pas concerné par la question".<p> Cette différenciation permet de connaitre les élèves pour lesquels ont doit renseigner l'enquête (réponse non renseignée) à partir de liste intitulé "Enquête"<p>