<p><u>Paramétrage de l'Enquête</u></p>

<p> cette action consiste à créer les questions et les réponses possibles pour chaque question </p>
<p> Concernant les réponses <u>le champ degré</u> permet de préciser si la réponse doit être affichée en
fontion du degré de l'établissment scolaire de la scolarité en cours de l'élève.</p>
<p>Ce champs peut prendre les valeurs 1,2, et 0.</p>
<p><u>Si la valeur renseignée des réponses est 0</u>, toutes les réponses seront affichées quelque soit le degré de l'établissement
scolaire de la scolarité en cours.il faut préciser que dans ce cas toutes les réponses possibles à une question doivent avoir la valeur 0.</p>

<p><u>Si la valeur renseignée et 1 ou 2 </u>,les réponses affichées seront celle dont le degré correspond au degré de l'établissment scolaire de la scolarité en cours de l'élève.
Il faut préciser que dans ce cas toutes réponses possibles à une question devront avoir le champs degré renseigné avec les valeur 1 ou 2.</p>